
#include <iostream>

int main() {

    // arithmetic operators = return the result of a specific arithmetic operation 
    //                  ( + - * / )

    int students = 20;

        /// students = students + 1;

    // Shorthand syntax:
        /// students += 1;

    // increment by 1 syntax:
    students ++;

    std::cout << students;

    // MARK: - Subtraction

        /// students = students - 1 ;

    // Shorthard syntax for subtraction:

        /// students -= 1;

    // Decrement by 1 syntax: 

    students --;
    

    return 0;

}